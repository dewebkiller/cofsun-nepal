                    <img class="img-fluid" src="images/img/REDD-awareness.jpg" alt="">
                    <p>Bilateral REDD+ initiatives in Nepal are being implemented through various forest management regimes particularly at local and sub-national scale. Preliminary evidences of such pilot projects have revealed that effective, equitable and efficient implementation of REDD+ is one of the key prerequisites to address the multiple social, environmental and economic concerns embedded within the forest management practices in Nepal. Balancing effectiveness, efficient and equity in REDD+ implementation and benefit sharing of REDD+ among relevant stakeholders becomes a profound task while forests in Nepal remain a major concern for several stakeholders with incompatible purposes and interests. To overcome such contradicting interests in REDD+ implementation, a consistent commitment and meaningful involvement of key stakeholders particularly the government authorities, civil society and private sector organizations is essential. Evidences of preliminary readiness REDD+ initiatives further indicate that a common understanding among stakeholders on REDD+ is critical to design commonly accepted country strategies, policy and sustainable implementation of REDD+ initiative. Fostering stakeholders’ meaningful participation in REDD+ is likely impossible unless and until the related stakeholders internalize the basic concept, procedures and associated elements of REDD+.</p>
                    <p>Some capacity building activities including awareness campaign, consultation workshops, interactions and training have already been carried out targeting relevant stakeholders at the local, district and national level. However, the majority of the stakeholders and their members especially at local and district level where REDD+ activities are executed, still lack even basic concept and knowledge of REDD+ initiatives and its development process both at international and national scale. Awareness on REDD+ process especially to district and local stakeholders does not just enhance their knowledge and understanding, this will more promote their realization on the significance of their engagement on REDD+ process.</p>
                    <p>Overall objective this assignment was to conduct REDD+ awareness targeting district level and local REDD+ stakeholders particularly newly elected members of local governments. Specific objectives of the assignment were:</p>

                    <p>To enhance participants’ knowledge on basic science of climate, context of change in climatic conditions and role of forests to climate change
                        To make district and local REDD+ stakeholders including newly elected members of the local government aware on global emergence of REDD+ and its development process in Nepal
                        To enhance understanding of REDD+ process thereby strengthen their capacity to identify their role and participation in REDD+ implementation process in Nepal
                        To encourage participants particularly newly elected members of local government to integrate and align REDD+ supportive activities into local government plan.

                    </p>

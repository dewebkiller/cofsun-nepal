                    <h1 class="mb-3">REDD+ TOT and Workshop</h1>
                    <img class="img-fluid" src="images/img/REDD-TOT-and-Workshop.png" alt="">
                    <p>The global policy mechanism “REDD+” is evolving not only as a means of reducing emissions from deforestation and forest degradation but also enhancing forest carbon stocks by promoting conservation and sustainable management of forests in developing countries. REDD+ is being promoted strongly by the World Bank and UN. The Forest Carbon Partnership Facility (FCPF) of the World Bank assists developing countries to be prepared for the implementation of REDD+. Nepal has been getting grant for its REDD+ readiness process that includes policy formulation, governance reform and capacity development.</p>
                    <p>Countries’ capacity to develop and implement performance-based REDD+ projects has been considered as one of the major components of REDD+ readiness. Extensive awareness and capacity building activities are important in REDD readiness stage (phase one) to prepare Nepal for effective implementation of REDD+ activities in the demonstration phase (phase second). Some capacity building initiatives that include awareness campaign, consultation workshops and trainings are already in place in Nepal and the REDD Implementation Center (REDD-IC) is leading this process in collaboration and coordination with civil society organizations, NGOs and development agencies. Consultation and outreach activities have emerged as a foundation for capacity building of REDD related stakeholders. The training of trainers (ToT) followed by district level workshops has been envisioned as an entry point to lay cornerstone for capacity building and outreach activities in upcoming years under the FCPF and other bilateral funding.</p>

                    <p>The main objective of this assignment was to enhance knowledge and skills of forest officers and civil society actors working at districts to deliver training on REDD+ and its status in Nepal applying ToT approach and workshops. The specific objectives are:</p>
                    <p>to deliver an updated and comprehensive REDD+ training package that incorporate staining resources on different REDD+ themes and lead REDD-trainer’s network
                        to enhance the capacity of relevant stakeholders to deliver training on REDD+
                        to improve understanding of DFO’s team and other district level stakeholders on REDD+ and its status in Nepal.</p>

                    <h3>Skill Test Level-I</h3>
                    <p>Literate with knowledge and skill in the relevant occupation with minimum of one year work experience in a related occupation/trade.<br>
                        Or<br>
                        Successful completion of one month (160 Hours) vocational training in relevant occupation/trade.<br>
                        Or<br>
                        Vocational training with six months’ work experience in the relevant occupation/trade.</p>
                        <br>
                    <h3>Skill Test Level –II</h3>
                    <p>Literate with knowledge and skill in the relevant occupation with minimum of three tears word experience in a relevant occupation/trade.<br>
                        Or<br>
                        One year training (min. 600 hrs theory and 800 hrs practical) in relevant occupation/trade.<br>
                        Or<br>
                        One year’s work experience after the sevel-1 ST certificate passed in relevant occupation/trade.</p>

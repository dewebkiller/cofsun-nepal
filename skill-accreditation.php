                    <h2>Skill Testing System in Nepal</h2>
                    <p><strong>Background:</strong><br>

                        The availability of skilled human resources is an important aspect for the economic development of nation. The quality and standard of skills learned from formal, non-formal and informal means is also important for Nepal’s workforce to make them competitive in national and international labor market. The traditional craftsmen, occupational groups, informally and/ or non-formally trained people and semi-skilled workforce of the industry need their skills certified by an agency recognized by private &amp; public sector. The National skill Testing Board (NSTB) of Council for Technical Education and Vocational Training (CTEVT) has the mandate as well as experience of skill testing &amp; certification.</p>

                    <p><strong>Introduction:</strong><br>
                        Skill testing activity was initiated in Nepal in 1983 through an autonomous body called skill Testing Authority (STA), which introduced a system of occupational classification, development of skill standards, skill testing and certification based upon the guidelines of Asia and pacific Skill Development program/ International Labor Organization (APSDEP/ILO). After the CTEVT was constituted in 1989, the STA was placed under the umbrella of CTEVT as Skill Testing Division (STD). Since them the division has been functioning as the secretariat of National Skill Testing Board (NSTB), which has been attached to the council. Till date, NSTB has developed occupational skill standards in 153 different occupations and 10675 crafts persons are certified.</p>
                    <p><strong>Occupational Skill Standard:</strong><br>
                        An Occupational Skill Standard is written specification of the practical skills, underlying knowledge, and experience demonstrated by an individual in a particular occupation.</p>
                    <p><strong>Skill Test:</strong><br>
                        A skill test is the corresponding performance test based on the occupational skill standard which must be demonstrated by every individual to obtain a “NATIONAL SKILL CERTIFICATE” indicating that the certificate holder meets the requirement of a trade/ occupation.</p>
                    <p><strong>Target Group of Skill Test:</strong><br>
                        Occupational/ Trade skill tests are open to all skilled workers and crafts persons. Principally, skill testing reaches out to three different target groups:</p>
                    <ul>
                        <li>Pass- outs from pre- employment vocational training wishing to facilitate their transition to work</li>
                        <li>Experienced workers (employed or unemployed) lacking an official certification</li>
                        <li>Workers who, at the end of in-service training, seek an upgraded or additional certificate</li>
                    </ul>
                    <br>
